import { readFileSync, writeFileSync } from "fs";
import { dirname, join } from "path";
import { fileURLToPath } from "url";
import { isR2Configured, purgeR2Bucket, uploadToR2 } from "../lib/r2.js";
import { SEED_MEDIA_ASSETS } from "./seed-media-data.js";
import { renderSeedSvg } from "./seed-media-svg.js";

const __dirname = dirname(fileURLToPath(import.meta.url));
const URLS_FILE = join(__dirname, "seed-media-urls.js");
const DATA_FILE = join(__dirname, "seed-media-data.js");

/** @param {import("@prisma/client").PrismaClient} prisma */
export async function clearMediaFromDatabase(prisma) {
  await prisma.vehicleModel.updateMany({ data: { heroMediaId: null } });
  await prisma.heroSlide.updateMany({ data: { imageMediaId: null } });
  await prisma.newsPost.updateMany({ data: { featuredMediaId: null } });
  await prisma.featureSection.updateMany({ data: { imageMediaId: null } });
  await prisma.siteSettings.updateMany({ data: { logoMediaId: null, faviconMediaId: null } });
  await prisma.policyDocument.updateMany({ data: { pdfMediaId: null } });
  return prisma.mediaAsset.deleteMany({});
}

/** @param {import("@prisma/client").PrismaClient} prisma @param {{ table: string, entityId: string, field: string }} link @param {string} mediaId */
async function linkMedia(prisma, link, mediaId) {
  const data = { [link.field]: mediaId };
  if (link.table === "vehicleModel") {
    await prisma.vehicleModel.update({ where: { id: link.entityId }, data });
    return;
  }
  if (link.table === "heroSlide") {
    await prisma.heroSlide.update({ where: { id: link.entityId }, data });
    return;
  }
  if (link.table === "newsPost") {
    await prisma.newsPost.update({ where: { id: link.entityId }, data });
    return;
  }
  throw new Error(`Unsupported media link table: ${link.table}`);
}

/** @param {Array<{ id: string, r2Key: string, publicUrl: string }>} results */
function writeSeedMediaArtifacts(results) {
  const lines = [
    "// AUTO-GENERATED by npm run db:seed:media — do not edit by hand",
    `// Generated: ${new Date().toISOString()}`,
    "",
    "/** @type {Record<string, { id: string, r2Key: string, publicUrl: string }>} */",
    "export const SEED_MEDIA_URLS = {",
  ];

  for (const row of results) {
    lines.push(
      `  "${row.id}": { id: "${row.id}", r2Key: "${row.r2Key}", publicUrl: "${row.publicUrl}" },`
    );
  }

  lines.push("};", "");
  writeFileSync(URLS_FILE, lines.join("\n"), "utf8");

  let manifest = readFileSync(DATA_FILE, "utf8");
  for (const row of results) {
    const re = new RegExp(`(id: "${row.id}"[\\s\\S]*?publicUrl: )"[^"]*"`, "m");
    manifest = manifest.replace(re, `$1"${row.publicUrl}"`);
  }
  writeFileSync(DATA_FILE, manifest, "utf8");
}

/**
 * Purge R2 + DB media, re-upload from SEED_MEDIA_ASSETS, link entities, write seed-media-urls.js
 * @param {import("@prisma/client").PrismaClient} prisma
 */
export async function runSeedMedia(prisma) {
  if (!isR2Configured()) {
    throw new Error("R2 not configured. Set STORAGE_S3_* in web/.env");
  }

  console.log("Purging R2 bucket…");
  const deletedKeys = await purgeR2Bucket();
  console.log(`  deleted ${deletedKeys} object(s) from R2`);

  console.log("Clearing media from database…");
  const { count } = await clearMediaFromDatabase(prisma);
  console.log(`  removed ${count} MediaAsset row(s)`);

  console.log("Uploading seed media…");
  const results = [];

  for (const entry of SEED_MEDIA_ASSETS) {
    const svg = renderSeedSvg(entry.svg);
    const buffer = Buffer.from(svg, "utf8");
    const publicUrl = await uploadToR2(entry.r2Key, buffer, "image/svg+xml");

    await prisma.mediaAsset.create({
      data: {
        id: entry.id,
        r2Key: entry.r2Key,
        publicUrl,
        folder: entry.folder,
        mimeType: "image/svg+xml",
        sizeBytes: buffer.length,
        altText: entry.altText,
      },
    });

    await linkMedia(prisma, entry.link, entry.id);
    results.push({ id: entry.id, r2Key: entry.r2Key, publicUrl });
    console.log(`  ${entry.id} → ${publicUrl}`);
  }

  writeSeedMediaArtifacts(results);
  console.log(`Wrote ${URLS_FILE} and updated publicUrl in seed-media-data.js`);

  return results;
}
